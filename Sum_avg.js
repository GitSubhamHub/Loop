//Example: find out the total sum, average between 1 to 10 including 1 and 10

var sn=1, sum=0;

while (sn <= 10) {
    sum = sum + sn
    sn++
}
console.log("Total: "+sum);
console.log("Average: ", sum/10);


//find out the product
var sp=1, product=1
while (sp <= 10) {
    product = product*sp
    sp++
}
console.log("Product: "+product);
